void lb_kbd_intr(void)
{
	UBYTE key;
    msg_in_t* pMsg;

	inportb(0x64);

	key = inportb(0x60);
	if (key < 0x40 && (pMsg = msg_accept_msg(&ExInputIO)) != NULL){
        pMsg->bMsgData[0] = kbd_ascii[key];
        pMsg->dwMsgLength = 1;
        pMsg->dwMsgType = MSG_IO_IN;
        msg_send_msg_intr(&ExTty,(msg_t*)pMsg);
    }
    sched_reschedule();
    outportb(0x20,0x20);

}

